package GsonChange;

public class Ying {

    private String Type;
    private String Fa;

    public Ying(String type, String fa){
        super();
        this.Type = type;
        this.Fa = fa;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getType() {
        return Type;
    }

    public void setFa(String fa) {
        Fa = fa;
    }

    public String getFa() {
        return Fa;
    }
}
